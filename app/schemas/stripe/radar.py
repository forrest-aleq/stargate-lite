# ============ REVIEWS ============

REVIEW_RETRIEVE = CapabilitySchema(
    capability_key="stripe.radar.review.retrieve",
    service="stripe",
    category="radar",
    description="Retrieve a Radar review",
    description_detailed=(
        "Retrieves a review generated by Radar when a payment is flagged "
        "for manual review based on your rules."
    ),
    parameters={
        "review_id": ParameterSchema(
            type="string",
            required=True,
            description="Review ID",
            example="prv_ABC123",
        ),
    },
    returns={
        "id": ReturnFieldSchema(type="string", description="Review ID"),
        "charge": ReturnFieldSchema(type="string", description="Charge ID"),
        "payment_intent": ReturnFieldSchema(type="string", description="PI ID"),
        "reason": ReturnFieldSchema(type="string", description="Review reason"),
        "open": ReturnFieldSchema(type="boolean", description="Is open"),
        "opened_reason": ReturnFieldSchema(type="string", description="Why opened"),
        "closed_reason": ReturnFieldSchema(type="string", description="Why closed"),
    },
    errors=[
        ErrorHint(
            error_code=ErrorCode.CREDENTIALS_MISSING,
            description="Stripe API key not configured",
            recovery_hint="Set STRIPE_SECRET_KEY environment variable",
        ),
    ],
    idempotent=True,
    has_side_effects=False,
)

REVIEW_APPROVE = CapabilitySchema(
    capability_key="stripe.radar.review.approve",
    service="stripe",
    category="radar",
    description="Approve a Radar review",
    description_detailed=(
        "Approves a payment that was placed in review. The payment will proceed."
    ),
    parameters={
        "review_id": ParameterSchema(
            type="string",
            required=True,
            description="Review ID to approve",
        ),
    },
    returns={
        "id": ReturnFieldSchema(type="string", description="Review ID"),
        "open": ReturnFieldSchema(type="boolean", description="Should be false"),
        "closed_reason": ReturnFieldSchema(type="string", description="Should be approved"),
    },
    errors=[
        ErrorHint(
            error_code=ErrorCode.VALIDATION_ERROR,
            description="Review already closed",
            recovery_hint="Only open reviews can be approved",
        ),
    ],
    idempotent=False,
    has_side_effects=True,
)

