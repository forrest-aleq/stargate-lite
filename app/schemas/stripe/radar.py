"""
Stripe Radar Capability Schemas.

Rich metadata for fraud prevention and risk management.
"""

from app.errors import ErrorCode
from app.schemas.base import (
    CapabilitySchema,
    ErrorHint,
    ParameterSchema,
    ReturnFieldSchema,
)

# ============ EARLY FRAUD WARNINGS ============

EARLY_FRAUD_WARNING_RETRIEVE = CapabilitySchema(
    capability_key="stripe.radar.early_fraud_warning.retrieve",
    service="stripe",
    category="radar",
    description="Retrieve an early fraud warning",
    description_detailed=(
        "Retrieves an early fraud warning indicating a charge is potentially "
        "fraudulent. Issued when card networks report suspected fraud."
    ),
    parameters={
        "warning_id": ParameterSchema(
            type="string",
            required=True,
            description="Early fraud warning ID",
            example="issfr_ABC123",
        ),
    },
    returns={
        "id": ReturnFieldSchema(type="string", description="Warning ID"),
        "charge": ReturnFieldSchema(type="string", description="Charge ID"),
        "payment_intent": ReturnFieldSchema(type="string", description="PI ID"),
        "fraud_type": ReturnFieldSchema(type="string", description="Type of fraud"),
        "actionable": ReturnFieldSchema(type="boolean", description="Is actionable"),
        "created": ReturnFieldSchema(type="integer", description="Unix timestamp"),
    },
    errors=[
        ErrorHint(
            error_code=ErrorCode.CREDENTIALS_MISSING,
            description="Stripe API key not configured",
            recovery_hint="Set STRIPE_SECRET_KEY environment variable",
        ),
    ],
    idempotent=True,
    has_side_effects=False,
)

